function surrogate = substitute(c, m)
  hs = HotSpot.Surrogate('floorplan', c.system.floorplan, ...
    'config', c.temperature.configuration, 'line', c.temperature.line);

  surrogate = hs.construct(c.power.Pdyn, ...
    'Unom', c.process.Unom, 'Udev', c.process.Udev, ...
    'leakage', c.leakage.model, 'process', c.process.model, ...
    'spaceMeasurementIndex', m.spaceMeasurementIndex, ...
    'timeMeasurementIndex', m.timeMeasurementIndex, ...
    'method', c.surrogate.method, ...
    'methodOptions', c.surrogate.options);
end
